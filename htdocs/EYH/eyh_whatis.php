<b><font color="#cb6940" size="+1">What is EYH?</font></b>


<P>
Expanding Your Horizons (EYH) Conferences offer a unique - and fun! - opportunity for young women in middle and high school to
explore their career options through small hands-on technical career workshops
led by female professionals from their local community who have found satisfying careers in law, mathematics,
engineering, computing, medicine, and other technical and non-traditional careers.
The conferences also include a component for the teachers and other adults who accompany the students to EYH.

