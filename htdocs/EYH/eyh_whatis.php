<P> NMNWSE has held several <a href="eyh_confs.php">EYH conferences</a> each year since about 1980, often in March to
coincide with <a href="http://www.nwhp.org/whm/index.php">Women's History Month</a>. Details for the current year conferences are on the pages above. We are in the process of adding more - contact the <a href="mailto:NMNWSE_Board-l@list.unm.edu ?Subject=EYH interest">NMNWSE Board</a> if you'd like to sponsor or be involved.  The NMNWSE EYH conferences are licensed from the
national <a href="http://www.expandingyourhorizons.org">Expanding Your Horizons Network</a> by  the <a href="http://nmnwse.org/">New Mexico Network for Women in Science and Engineering</a> (NMNWSE), as part of NMNWSE's mission to encourage the the entry into and career development of women in science and engineering professions. Conference attendees are given a copy of
NMNWSE's <a href="http://nmnwse.org/careers"> Career Book</a>, along with student and adult information packets developed by the national EYH Network, and other career information which emphasizes that taking math and science courses in school  opens the doors to a wide variety of rewarding careers.





