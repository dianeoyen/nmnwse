<b><font color="#cb6940" size="+1">How Can I Help?</font></b>


<P>
If you would like to donate money or in-kind items, update or contribute a section to our <a href="http://nmnwse.org/careers">online Careers Book</a>, volunteer to help with one of the 
<a href="index.php">NMNWSE EYH Conferences</a>, start a new EYH in New Mexico under our site license, or help in any other way, please contact the NMNWSE Board at <a href="mailto:NMNWSE_Board-l@list.unm.edu">NMNWSE_Board-l@list.unm.edu</a>. See also the sidebar link to <a href="index.php">Conferences &amp; Contacts</a> for the local conference chairs if you wish to help locally.

<P>We can use anything from people who can volunteer a few hours of their time on the day of the conference to people who can be active participants on the organizing commitees,  from a single logo item advertizing a business that can be used as a door prize to enough materials for all conference attendees, from a few hardcopies of career booklets for teachers who attend our associated adult components to permission to add it to the CD with our Careers book we give all atendees. And of course, we can use financial donations of any size. Major statewide donors are featured on the Conference Tote Bag given to all attendees. We also try and stretch the value of your donation by making our conference purchases from New Mexico businesses which actively support diversity.


<P> All NMNWSE EYH organizers are unpaid volunteers, and NMNWSE is a registered non-profit 501(c)(3) Organization.

<P>
<img src="http://nmnwse.org/Images/bl_pin.gif">Monetary donations can be made to NMNWSE via credit card through the Network for Good.
