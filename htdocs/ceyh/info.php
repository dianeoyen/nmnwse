<P>Students meet with women who have found satisfying careers in law, mathematics, engineering, computing, medicine, and other non-traditional technical careers. Students explore these career opportunities in hands-on, career-related workshops. They learn what jobs are available, what preparation and training they require, and how successful women are excelling in mathematics and science-related fields today!</P>
          <P>
Each student attends three sessions where they break out into small workshops highlighting a variety of technical careers, each lead by a professional working in that field who shares an aspect of her (or his) technical field through a hands-on activity and serves as a role model. Additionally, the workshop presenter will share what education and skills are required to perform her job, what she does at her job, and why she chose her particular career. Workshops typically have 10 to 15 students, facilitating interaction between the presenter and students, and include opportunities for questions and answers.
</P>

<P><img src="http://nmnwse.org/Images/bl_pin.gif"><b>A separate <a href="adult_program.php">adult program</a> is provided for adults who wish to attend EYH.</b> Advanced registration and $5 fee is required to participate in the adult program. 
Adults may not attend the student program.
