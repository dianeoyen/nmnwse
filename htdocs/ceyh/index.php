Here is your chance to meet with female professionals from the community who have found satisfying careers in law, mathematics, engineering, computing, medicine, and other non-traditional technical careers. Each student attends three workshops, led by a woman professional who shares an aspect of her technical field. Workshops typically have 10 to 15 students; which facilitate interaction between the presenter and students. Presenters also share personal experiences and other influences that have impacted their decisions as they have developed their careers.
