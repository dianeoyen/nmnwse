The 2010 Albuquerque EYH conference features two adult programs held in parallel with the student career workshops.
Both programs begin with the Keynote Event, <a href="keynote.php">a panel discussion featuring four dynamic young women who have chosen a technical career field</a>, which the students also attend. Then the the programs separate into 
a for-credit course targeting Educators and Counselors, and a more informal program targeting Parents and other adults who accompany students to EYH. Both Programs re-join the students for the Closing Session.
Participation by accompanying adults in either of these adult programs is optional,
but registration ($5) is required for those who wish to attend, and 
accompanying adults are not permitted to attend the Student Workshops.


