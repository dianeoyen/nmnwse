<b>"printable" or "<img src="Images/icon_print.gif">w/o navigation" link does't go to right page.</b>
<P>
On some browser this will not work. To see the printer-friendly version of any of our pages ending in php, add at the end of the URL you wish to view in your browser's navigation window  the string:<br>
&nbsp; &nbsp; &nbsp; &nbsp; ?printable=1<br>
For instance this page is <br>
&nbsp; &nbsp; &nbsp; &nbsp; <a href="http://nmnwse.org/web_faq.php">http://nmnwse.org/web_faq.php</a>
<br>The printer-friendly version is <br>
&nbsp; &nbsp; &nbsp; &nbsp; <a href="http://nmnwse.org/web_faq.php?printable=1">http://nmnwse.org/web_faq.php?printable=1</a>
<br>Try these and see the result!

