<P><B>History</B>: To help increase public awareness of the impact of light pollution on the scientific value of dark night skies, NMNWSE voted in 2004 to consider adopting a Dark Sky Resolution.
The above resolution was adopted by vote of the membership  at the 2006 NMNWSE Annual Meeting.

<P><B>Light Pollution Definition</B>:
Any adverse effect of artificial light including sky glow, glare, light trespass, light clutter, decreased visibility at night, and energy waste. 
See also the
<a href="http://en.wikipedia.org/wiki/Light_pollution"</a>Wikipedia light pollution entry</a>.

