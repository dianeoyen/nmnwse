<P><B><a href="http://www.darksky.org">International Dark Sky Association</a></B>
<br>The <a href="http://www.darksky.org">International Dark Sky Association</a> (IDA) is generally recognized as an authority on light pollution. The IDA has recently entered into an agreement with the <a href="http://www.iesna.org/">Illuminating Engineering Society of North America</a> (IESNA), the recognized technical authority on illumination in the U.S., to develop a <a href="http://www.darksky.org/ordsregs/mlc/">Model Lighting Ordinance (MLO) and companion Design Guidelines</a> for regulating outdoor lighting appropriate to the needs of communities, the environment, and the natural habitat.


