	<h2>2010: Eleanor Walther, Albuquerque</h2>
	<img src="http://nmnwse.org/ImpactAward/winners/Walther2010_small.jpg">
	<p>
	  Eleanor Walther has been a member of NMNWSE for almost 30 years and served in a variety of
	  positions, including statewide president. Since 1991, she has been on the organizing committee for
	  the Albuquerque Expanding Your Horizons conference for 6th-12th grade girls to encourage them to
	  pursue non-traditional careers. Additionally Ms. Walther has been involved in the Supercomputing
	  Challenge, a year long program for mid and high school students to promote math and science, and Project GUTS
	  (Growing Up Thinking Scientifically), a summer and after-school science, technology, engineering and math (STEM) program for middle school students. In June 2010, Eleanor worked with 21 female teachers
	  with limited computer modeling and programming skills to to become GUTS
	  Club leaders and/or Supercomputing Challenge partners -- Ms. Walther's depth of knowledge and
	  friendly, comfortable personality made the training a success.
	  "Eleanor is an exemplary model in her extraordinary efforts in furthering the goals of the New
	  Mexico Network for Women in Science and Engineering," states Celia Einhorn, program manager of
	  the Supercomputing Challenge. "Her outstanding participation in both Project GUTS and the
	  Supercomputing Challenge has been an exemplar for women for over a decade."
	</p>
