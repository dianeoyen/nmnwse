	<h2>2013: Phyllis Baca, Santa Fe</h2>
	<img src="http://nmnwse.org/ImpactAward/winners/Baca2013_small.jpg">
	<p>
	  Phyllis Baca's passion is to present STEM opportunities to low-income students, especially women and
	  Hispanics.  Around 2007, Phyllis began volunteering at Sante Fe Community College with
	  a goal to double the number of women and Hispanics enrolled in the engineering department.
	  Phyllis focused on revitalizing the Engineering and Engineering Technologies degree programs
	  through clear career pathways tied to State and National career clusters and more than succeeded
	  in her goal, quickly rising to department chair. 
	  "As a teacher, Phyllis has been an inspiration. She is a role model that I could understand. Her passion as an instructor has always shined through, from the first time I met her in my Introduction to Engineering course to her help and advice today." wrote Erika Arvizo, one of the first of the women to receive her Associates of Science in General Engineering at Santa Fe Community College.
	  Through her teaching at Santa Fe Community College's engineering department and
	  volunteering with numerous local, state, and regional agencies such as Project Lead the Way and
	  Expanding Your Horizons, Phyllis has touched the lives of hundreds of NM educators and
	  thousands of NM students, and has dramatically increased the number of women and Hispanics
	  obtaining STEM degrees in New Mexico.
