<h2>
<font color="#4b9fc3">NMNWSE Careers CD</font><br>
</h2>

<P>
Each year NMNWSE produces a "Careers CD" featuring our <a href="http://nmnwse.org/careers">Careers Book</a> along with related material we have received permission to reproduce. We give all students who attend NMNWSE <a href="http://nmnwse.org/eyh">Expanding Your Horizons (EYH) conferences</a> a copy of the CD, as well as the winners of our awards at science fairs and similar STEM student competitions. As funds are available to support the CD printing, we also try to provide copies to give away at various STEM and career student events in New Mexico.  

<P>If you would like to suggest additional content, donate to support printing of this CD or NMNWSE's educational outreach efforts, or would like a sample copy of the CD, please contact the NMNWSE Board at <a href="mailto:NMNWSE_Board-L@list.unm.edu">NMNWSE_Board-L@list.unm.edu</a>.
or PO Box 1360, Los Alamos, NM 87544.

