<p><b><font size=+1>We are the New Mexico Network for Women in Science
and Engineering (NMNWSE),</font></b> &nbsp;a nonprofit association of women
who are scientists, engineers, educators, parents, community leaders, and
professionals in business, government, and industry.
The mission of NMNWSE is to encourage women to enter into careers in science,
technology, engineering, mathematics, medicine, and allied professions (STEMM),
and to recognize and encourage women's achievements in STEMM careers.

<P>
NMNWSE holds annual conferences around New Mexico to introduce young women
in their teens to STEMM careers.
These conferences are called "Expanding Your Horizons," and they were
originally conceived by and are licensed through the national EYH Network,
<a href="http://www.expandingyourhorizons.org">http://www.expandingyourhorizons.org/</a>.
During Expanding Your Horizons conferences young women are given the opportunity to engage in hands-on activities and meet successful women who are working in STEMM careers. 

<p><b><font size=+1>Why this book? </font></b>What do you want to be when
you grow up? Chances are when children answer this question their responses
are limited to the careers of the people around them. We want to expand
the possible answers to this question by introducing young women to all
of the women who contributed to this book. Each author has provided insights
into her own career, and the enthusiasm she feels comes through in each
description. We hope that this book will spark the interest in all our
readers, but particularly those for whom this was written - young women
exploring their possibilities.
<p>Thank you to all the authors who dedicated their time and efforts to
see that the women that come after them have a headstart at a fulfilling
career, as well as to the NMNWSE members and friends who helped with editing, organizing, updating, and publishing of the current as well as previous editions.
