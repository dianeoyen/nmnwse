Conference-day volunteers are always needed, especially during morning registration. Sign up for a couple of hours, up to the whole day. One of our most important volunteers are workshop assistants, known as "gofers."<p>

Gofers are primarily another adult in the room, to help the workshop presenters cope with any little last minute details or snafus, help supervise the students, and maybe learn something interesting themselves.<p>

Gofers also get to help with the conference-opening team activity, as we get the girls involved with meeting new people and working together to solve a problem.<p>

If you're not sure what or how to present a workshop, being a gofer is another good way to get ideas for a future year! <p>


Please contact Georgia Pedicini, (505)667-8117 gap@lanl.gov, or any <a href=http://nmnwse.org/lawis/eyh/contact/>planning committee member<a/> for more info.<p>

