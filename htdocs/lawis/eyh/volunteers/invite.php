Dear Colleagues,<p>
The Northern Chapter of the New Mexico Network for Women in Science and Engineering (also known as LAWIS (Los Alamos Women in Science)) invite you to participate in our 29th Annual Expanding Your Horizons Conference. Co-sponsored by the Los Alamos National Laboratory, this year's EYH Conference is scheduled for April 2, 2008, at the University of New Mexico-Los Alamos campus. We host young women in grades 8-10 from throughout northern New Mexico and encourage them to become the next generation of scientists, engineers, and mathematicians.
<p>						
EYH goals are: <br><ul>
	<li>To provide young women with opportunities to meet and interact with positive women role models who are active in math and science related careers; <br>
	<li>To involve young women with limited opportunities for success in positive experiences in mathematics and science; <br>
	<li>To increase the interest of young women in math and science by providing exciting and fun hands-on learning experiences; <br>
	<li>To encourage young women to study as much math and science as possible by showing them the benefits of education and its relevance to their lives; <br>
	<li>To foster awareness of career opportunities in science and math related careers. <br></ul>
<i>(1999/2000 Expanding Your Horizons in Science and MathematicsTM Organizational Packet Materials, &copy; 2000 The Math/Science Network)</i><p>

The 1995 AAUW report Growing Smarter recognized that three of the key factors in girls' academic success are: (1) a hands-on approach to learning, (2) single sex setting for non-traditional activities, and (3) exposure to women role models. EYH strongly emphasizes these three elements.<p>

We are currently looking for volunteers in the following areas:
Workshop Presenters: to offer hands-on and interactive workshops in your area of scientific expertise. Contact: Georgia Pedicini (gap@lanl.gov)<p>

Logistical Assistants: to assist the Workshop Presenters and/or to serve as escorts for the young women on the day of the conference. Contact: Georgia Pedicini (gap@lanl.gov)<p>

Planning Committee Members: to help the Planning Committee prepare the conference. Contact: Aviva Sussman (spring@lanl.gov)<p>

LAWIS is a non-profit organization. To make a donation, please contact: Wendee Brunish (wb@lanl.gov). More information can be found at: www.nnmwse.org/eyh.<p>

Thank you for your support.<p>

