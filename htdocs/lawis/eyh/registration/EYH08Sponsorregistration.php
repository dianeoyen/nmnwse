<p align="center"><strong>TEACHER WORKSHOP REGISTRATION/SPONSOR  INFORMATION </strong>(<em>PLEASE PRINT OR TYPE CLEARLY</em>)</p>
<p><strong>Adult Sponsors: </strong>All students attending EYH08 must have an adult  sponsor, e. g. a teacher, counselor, parent, or guardian. The purpose of the  adult sponsor is to have someone to contact should a question arise as to the  eligibility of the student to attend EYH08, or to recommend the student if the  organizers are forced to choose between applicants due to space limitations. <em>The sponsors do not have to accompany their  students to EYH08. </em>However, if the sponsors so choose, they can register  for the same day Teacher Workshop using this form.</p>
