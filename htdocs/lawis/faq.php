On some browser this will not work. To see the printer-friendly version of any of our pages ending in php, add at the end of the URL you wish to view in your browser's navigation window  the string:<br>
&nbsp; &nbsp; &nbsp; &nbsp; ?printable=1<br>
For instance this page is <br>
&nbsp; &nbsp; &nbsp; &nbsp; <a href="http://nmnwse.org/lawis/faq.php">http://nmnwse.org/lawis/faq.php</a>
<br>The printer-friendly version is <br>
&nbsp; &nbsp; &nbsp; &nbsp; <a href="http://nmnwse.org/lawis/faq.php?printable=1">http://nmnwse.org/lawis/faq.php?printable=1</a>
<br>They these and see the result!

