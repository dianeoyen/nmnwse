<img src="http://nmnwse.org/Images/bl_pin.gif"><b>Call for Papers:</b>
<P>

Technical presentations on any topic in science and engineering (broadly defined to include most other fields), as well as presentations on other topics which may be of interest to women working in these fields, are welcome. 
<br>-<img src="http://nmnwse.org/Images/spacer.gif" width=1 height=18>
Talks are 30 minutes, including questions.
<br>-<img src="http://nmnwse.org/Images/spacer.gif" width=1 height=18>
Posters have no fixed size limits.
Due to the limited time available for talks, poster presentations are preferred.
<br>-<img src="http://nmnwse.org/Images/spacer.gif" width=1 height=18>
To submit a presentation, simply include the draft title and abstract with your meeting registration. You will be contacted for final title and abstract around 1 Oct.
After 1 Oct, only poster presentations are accepted. 
<br>-<img src="http://nmnwse.org/Images/spacer.gif" width=1 height=18>
Note students and members with exceptional circumstances giving talks or posters are eligible to apply for a meeting scholarship.

