<center>
<hr width="30%">
<B><FONT color="#cb6940" size="+2">AT-LARGE</b></font>
</center>

<P>
<img src="http://nmnwse.org/Images/button.gif">&nbsp;<font size="+1"><B>Yolanda King</b></font>
<br>
Dr. Yolanda King has 30 years experience as an USAF civilian in R&D management, primarily in directed energy and space technologies, with a background in Physics/Chemical laser kinetics. 
She was the first woman to chair the NATO panel on Sensors and Electronics Technologies.
Yolanda's main interest in the Network is to encourage young girls to enter nontraditional careers.
<br><I>Yolanda was NMNWSE Treasurer from 2003-2006, and NMNWSE Central Chapter member since 1982.</I>

<P>
<img src="http://nmnwse.org/Images/button.gif">&nbsp;<font size="+1"><B>Klaus Heinemann</b></font>
<br>
Dr. Klaus Heinemann is a post-doc in Mathematics at UNM.
<br><I> Klaus was a Central EYH Co-Chair in 2012. NMNWSE Central Chapter Member since 2011.</I>

<P>
<img src="http://nmnwse.org/Images/button.gif">&nbsp;<font size="+1"><B>Diane Oyen</b></font>
<br>
Ms. Diane Oyen is a PhD Candidate in Computer Science at the University of New Mexico. Her research area is in the analysis of large datasets using machine learning and artificial intelligence. 
<br><I>Diane is a current Board Member. NMNWSE Central Chapter Member since 2010.</I>

<P>
<img src="http://nmnwse.org/Images/button.gif">&nbsp;<font size="+1"><B>Tinka Gammel</b></font>
<br>
Dr. J. Tinka Gammel is theoretical Condensed Matter Physicist doing Equation of State work at Los ALamos National Lab. She moved the NMNWSE Careers book from hardcopy to the web, created the first "NMNWSE Careers CD" for the NMNWSE EYH conferences, and has overseen several revisions to the NMNWSE Careers Book and Careers CD.
<br><I> Tinka is the current NMNWSE webmaster and Communications Chair. NMNWSE Central Chapter. Member since 1996.</I>

