<P>
<img src="http://nmnwse.org/Images/button.gif">&nbsp;<font size="+1"><B>Barbara Torres</b></font>
<br>Ms. Barbara Torres is owner and CEO of Barbara Torres Consulting, a small, woman-owned business providing professional technical consulting services in the areas of business development, ethics programs, and other regulatory areas.  Ms. Torres established her company upon retirement from Northrop Grumman Corporation. Ms. Torres also has many years of active participation in such organizations as United Way, NMNWSE, and American Business Women' Assocation, and serving as a judge in many Science and Engineering Fairs.
<br><I>Barbara is the current NMNWSE Treasurer. NMNWSE Central Chapter Member since 1985.</I>

