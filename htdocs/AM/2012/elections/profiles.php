<img src="http://nmnwse.org/Images/button.gif">&nbsp;<font size="+1"><B>Pat Pettit</b></font>
<br> Dr. Patricia Pettit has over 20 years of aeronautical engineering research and development experience with industry and the USAF. She is currently the division chief for engineering resources at the Air Force Nuclear Weapons Center. Pat is a strong proponent for STEM recruitment.
<br><I> Current NMNWSE At-Large Board Member. NMNWSE Central Chapter Member.</I>


