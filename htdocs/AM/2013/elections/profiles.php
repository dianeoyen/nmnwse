<P>
<img src="http://nmnwse.org/Images/button.gif">&nbsp;<font size="+1"><B>Nichole King-Campbell</b></font> <br>
Nichole King-Campbell resides in Albuquerque New Mexico.   For the past eighteen years, the United States Air Force has employed her, as an active duty member, reservist and civilian employee.  During this time, she has sought to get more women and youth interested in the S.T.E.M career fields.     Nichole is passionate about reaching out to young women after noticing there were women missing from some of the career fields she interacted with.   After becoming involved with the Tuskegee Airmen Chapter, she noted the lack of women pilots in the organization.   She noted that some women including her were not necessarily steered toward those experiences. Now as a mother of two elementary school daughters, she has become even more committed to provide opportunities for young women everywhere to be excited about Science Technology Engineering, Mathematics and Health Career fields. 

<P>
Over the past several years, Nichole found herself questioning why she was not involved in career fields that she initially found intriguing.   Without mentors, an engineering career field was not something she strived towards.   As a result, any opportunity she has to work with organizations and projects which encourage girls to pursue these science career fields, Nichole wants  to be a part of that experience.       Nichole is determined to ensure any young women who think they may be interested in these amazing career fields, has an opportunity to get as much information and mentoring possibly.  Nichole had an amazing experience this past winter with EYH-ABQ.  She was able to experience first-hand the reactions of the young caterpillars that started in the morning and became butterflies as the day progress.  It was during that experience she realized that many adults including herself forgot how awkward the grades 5-9. It renewed her dedication to make sure young women will have support for their STEM pursuits.
<P>
Nichole is a Level 3 Life Cycle Logistician for the U.S. Air Force.   She has a Bachelor of Arts in Urban Legal Studies from The City College of New York, a Master of Science in Administration from Central Michigan University, and a Master of Science in Nonprofit Management and Philanthropy from the Graduate School at Bay Path College.   Not intimidated by the sciences anymore, Nichole is planning to pursue her PhD in Integrating Technology, Learning, and Psychology October 2013.
