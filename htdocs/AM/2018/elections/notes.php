      <h2>Notes:</h2>

      <h3>Why we ask for your name, and what we do with it</h3>
      <p>
        The elections form asks for your name so the Elections Chair can insure only members in good standing vote, and that they only vote once.
        Only votes from people who are verified by the Elections Chair to either currently be members or will become members by the date of the Annual Meeting will be counted. 
      </p>
      <p>
        The Elections Chair uses this information for the sole purpose of determining that the vote is valid.
        The only other person with access to this information is the webmaster, who will only access it at the request of the Elections Chair in the event of problems with the online voting form.
        No permanent record will be kept which associates your individual choices with your name.
        If you have any questions on the elections process, please contact the Elections Chair.
      </p>

      <h3>Voting on the Board positions</h3>
      <p>
        For each open position (1 Pres, 1 VP, 1 Treas, up to 5 2-yr At-Large), you may vote for a declared candidate listed on the ballot, or a write-in candidtate of your choice - please contact the Elections chair if you need space for more write-ins.  You may vote for a given person only once, and you must select a choice for each declared candidate (including not voting for them), to insure you did not forget to make a choice. You may vote for only 1 person for VP, only 1 person for Treasurer, and up to 5 people for At-Large, where "person" is to be interpreted as declared or write-in candidate. Voting for more than allowed will invalidate your ballot.
      </p>

      <h3>Required fields on the online voting form</h3>
      <p>
        To insure you did not accidentally skip casting one of your votes, you must select an option (which includes 'no vote') for each declared candidate before the form will be accepted. If votes are missing, it will send you to an error page, which will allow you to go back and finish the form. If the form is successfully submitted, you will be sent to a 'thank you for voting' page, which will forward you to the main Annual Meeting page after a few seconds.
      </p>

      <h3>How you know your vote was sucessfully submitted/received</h3>
