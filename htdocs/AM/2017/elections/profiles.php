<img src="http://nmnwse.org/Images/button.gif">&nbsp;<font size="+1"><B>Casey DeRaad</b></font>
<br>
Casey Anglada DeRaad is a strong advocate for academic, industry and community partnerships in New Mexico. She is a proven leader in her positions held with the Air Force Nuclear Weapons Center and Air Force Research Laboratory for Technology Engagement. Her adept skills have led to connecting partners in the community to engage with Air Force partners for academic, job and contracting opportunities. Ms. DeRaad develops strategies to support the local academic and industrial community and help them to take advantage of the $3.8B/annual economic impact of KAFB to the state of New Mexico. Formerly Ms. DeRaad was the Director of the Phillips Technology Institute and led the Technology Engagement office for the Air Force Research Laboratory in New Mexico, leading the technology engagement with industry, academia and government stakeholders as well as leading the state of New Mexicoâ€™s largest Science, Technology, Engineering and Math (STEM) programs reaching over 3000 K-12 students per year. Ms. DeRaad is a 10th generation New Mexican from Taos, New Mexico and earned her Bachelor of Science and Master of Science in electrical engineering from the University of New Mexico in 1985 and 1992 respectively.

<P>
<img src="http://nmnwse.org/Images/button.gif">&nbsp;<font size="+1"><B>Rajaa Shindi</b></font> <br>
Dr. Rajaa Shindi is Systems Administrator and an expert in SharePoint migration and management technologies, and a faculty member at Dona Ana Community College (DACC) teaching computer science and information technology courses.
She holds a PhD in Computer Science from NMSU where she worked on the research and development of a novel system for enhancing attention among children with Attention Disorder.
<P>
Facing many obstacles growing up, Rajaa is now a strong supporter of STEM, and is Co-PI on a 2015 STEM grant to promote STEM among Middle, High School and DACC students.  The grant is also to encourage the students to "think computationally" across disciplines to empower female students' capability and encourage them to major in STEM.    
She also helps with the Las Cruces EYH, volunteers at science fairs, and looks for other ways to help children with specific needs such as ADHD, physical handicapped, mental disorders, etc. 
