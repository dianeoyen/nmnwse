<P>
The <a href="http://www.vallescaldera.gov">Valles Caldera National Preserve</a> was created by Congress in 2000 from a well-known ranch known as “the Baca Ranch” in New Mexico’s volcanic Jemez Mountain Range. This 89,000 acre property is situated inside a collapsed crater and is studded with eruptive domes and large meadows. The scenic solitude of the preserve will leave you refreshed and relaxed.
<P> As the Preserve will be closed for the season during the meeting, activities in the Preserve are limited to walks within 300 meters of the Lodge. However, we will have at least one telescope with us and we recommend leaving plenty of time to just sit on the porch at the Lodge and enjoy the vista. Be aware there can be snow at the Lodge in October, so plan accordingly.

<P> Other area activities include fishing, elk-viewing, hiking, scenic car trips, visting local galleries, soaking in hot springs, etc - the <a href="http://www.jemezsprings.org">webpage for the Village of Jemez Springs</a> is a good place to start investigating options. You may also be interested in the <a href="http://www.lanl.gov/museum/">Science</a> and <a href="http://www.losalamoshistory.org/planvisit.htm">History</a> Museums in Los Alamos.

