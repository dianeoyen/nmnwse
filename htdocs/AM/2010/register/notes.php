<P>
<a name="accompany">
<B>Accompanying adult</b> (12+) registration for the full meeting includes the Friday evening reception, all meals and snacks Saturday including the wine and cheese reception during the poster session and the conference dinner, and breakfast Sunday. 
A reduced rate is offered for those who will only join us Saturday all day (except breakfast), or Saturday after lunch. 
<b>Accompanying children</b> (11 and under) only need to be included on the registration form if they will be eating with us (the only option on the Preserve), and can partake of all food for the "just conference dinner" price (free for children 3 and under, but please still indicate the number on the form to insure we have food for them). 
Accompanying persons 18 and under are welcome to attend the technical sessions at no charge (if interested). 
Accompanying adults over 18 wishing to attend the business meeting or technical talks would need to register as attendees; however, accompanying adults coming to the conference dinner may come to the poster session/pre-dinner reception without registering as attendees. 
If you select any "3+" persons option in the conference fees section, please use the accompanying person name field or the comment box at the end of the registration form to specify actual number(s), and multiply by the actual number(s) of people to obtain the total.

