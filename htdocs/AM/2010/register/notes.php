<P>
<B>Attendee registration</b> for the full meeting includes a Friday evening reception, Breakfast Saturday and Sunday, Lunch Saturday, snacks during the meeting, the wine and cheese reception during the poster session, and the conference dinner Saturday, plus covering our facilities costs. Although slightly reduced rates are offered for attendees who can only attend the technical portion, all attendees are encouraged to come for the full meeting. Registration includes meals and snacks as there are no concession options available in the Preserve. (A receipt with food listed as a seperate cost can be provided if required by your employer.)

