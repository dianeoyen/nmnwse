<P><img src="http://nmnwse.org/Images/button.gif" height="12"><B>SCHOLARSHIP NOTES</b><img src="http://nmnwse.org/Images/button.gif" height="12"> 
<a name="scholarship">

<P>Students, and members with exceptional circumstances, can apply for a meeting scholarship to cover costs.
The application process is simple: check the box on the registration form indicating you want to apply
for a scholarship, and provide a brief justification in the space provided (e.g., "student presenting paper" is sufficient for student applicants) -
we will contact you if more information is needed. 

<P>Scholarships are awarded based on need and whether
presenting a paper in the technical session. The scholarship covers the conference registration, receptions, and conference dinner.
In exceptional circumstances we may be able to provide full scholarships to cover lodging, all meals, and some transportation - please indicate in the justification if you
feel your circumstances justify a full scholarship. You can also contact the 
<a href="mailto:NMNWSE_Board-l@list.unm.edu ?Subject=NMNWSE AM2010 Scholarship">NMNWSE Board</a> or the
<a href="../contacts.php">Technical Chair</a> for more information on our scholarship policy.


<P>If applying for a scholarship, after submitting the online registration form, please wait to send any payment for your conference fees until you have heard from the <a href="http://nmnwse.org/AM/2010/contacts.php">meeting registrar</a> as to your scholarship status. Only send payment after the registrar has let you know your remaining balance after subtracting the scholarship award. Please give us at least one week to review your request. 
If you do not receive an email from the registrar acknowledging your online registration was received within two business days of submitting, or you do not hear within one week as to your scholarship status, or if you have questions, please <a href="http://nmnwse.org/AM/2010/contacts.php">contact us</a>. 

<P>We understand that some applying for scholarships may not be able to attend the meeting without it, and if your application is not approved we will not assess any penalities for cancelling your registration. We also encourage any who might fall into this category to indicate that on their application as a hardship consideration is part of our decision process. 


<P><img src="http://nmnwse.org/Images/button.gif" height="12"><B>ACCOMMODATIONS NOTES</b><img src="http://nmnwse.org/Images/button.gif" height="12">

<P>
Housing at the Preserve is limited and no direct individual room reservations are possible. Hence spaces will be assigned on a first-come, first served basis
and we encourage you to reserve your accommodations early. 
Also, the Preserve is otherwise closed during the meeting and so you will need to coordinate your arrival and departure with the meeting registrar so the gate can be opened.

The Lodge has 8 double rooms (2 twins, or king). The Bunkhouse has one "single" room (queen bed could be added), and two rooms with 6 beds each (6 single, or 2 full plus 2 single). 
<a href="http://nmnwse.org/AM/2010/AM2010_AccomodationsLayout.pdf">Click here for a sketch of the room layout.</a>
See also the <a href="http://nmnwse.org/AM/2010/hotel.php">conference hotel page</a> 
for more information. 

<P>
For those indicating they wish to stay at the Preserve (both Friday and Saturday nights - we are not offering a 1-night option),
the registrar will contact you in the order you registered to let you choose from remaining spaces. Adjustments to the registration fee to reflect actual lodging received vs desired preference indicated on the registration form will be made at the meeting.
Pets are not allowed.
<P>
Note the accomodations are reserved "per bed space", not "per room", so if you wish a room alone you will either need to be the first to request the one single, or pay for all bed spaces in the room of your choice - as bed space is limited we do not encourage this option. When the registrar contacts you, if your party does not fill a room you will be asked about roommate preferences - you can also specify that in the comment section on the registration form. 

<P>Note that if we do not have 
sufficient registration we may also have to cancel the bunkhouse - if the bunkhouse is cancelled, will will do our best to help any affected persons find suitable alternate accomodations, or refund their registration in full.


<P>
If you will not be staying on the Preserve, see the <a href="http://nmnwse.org/AM/2010/hotel.php">conference hotel page</a> for suggestions for housing in the nearby communities. Don't forget you will need to coordinate arrival and departure times to/from the Preserve with the meeting registrar as the Preserve gate is otherwise locked. 

<P>The Lodge is at 8800 feet, and snow is not uncommon in October. Hopefully we'll have beautiful warm fall days and cool clear nights, but freezing temperatures or a blizzard is also possible.  <B>Please plan to dress accordingly.</b>

<P><b>Room Sharing / Carpooling:</b> We plan to send emails out to those who check these boxes basically saying "The people on this email expressed interest in sharing a room/carpooling. Contact each other." The Registrar will take standard measures to insure all that the email goes out only to those who have a legitimate meeting-related interest. However, if you do not wish us to share your contact information with others who are similarly interested in a sharing a room or carpooling, be sure to indicate that in the comments section, or just don't check these boxes.

<P><b>Scholarship applicants:</b> 
We are holding some of the less expensive spaces in the bunkhouse for scholarship recipients, although we still recommend you register early as we may have to release these as the deadlines approach. 
