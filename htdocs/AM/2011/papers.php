</b>, or email the abstract to the <a href="contacts.php">Technical Chair</a>. The registration form has space to indicate your preference for poster or oral presentation, and a comment block to add any other information relevant to your presentation - if you send your abstract seperately, please note that on the registration form and also include your preferences in the email with your abstract.

<P>
<b>Poster presentations are preferred</b>
due to the increased networking we observe during the poster session, as well as the limited time available for talks.
The poster session is held in the early evening prior to the conference dinner, and appetizers are served during the poster session to encourage attendance and mingling.

<P>
If more talk submissions are received than time allows, the Technical Chair may relegate some talks to posters. If your employer requires you to give a talk in order to be reimbursed for attending, please note that on the registration form.
Post-deadline technical abstracts may be relegated to the poster session even if you indicate you prefer to give a talk. Please contact the Technical Chair if you have questions about the scheduling of your presentation.

<P> 
<b>Meeting scholarships are available.</b> If you are a student and submit an abstract, to apply for the standard scholarship (covers registration fees) simply
check the box on the registration form indicating you want to apply for a scholarship, and write "student presenting paper" in the space provided for the justification - no additional information is needed for students to apply, and no payment is due until you are notified of your scholarship status.
Members with exceptional circumstances can also apply, and a limited number of full scholarships may be available if circumstances merit.
