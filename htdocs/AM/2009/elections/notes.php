<b>Notes:</b>


<P><b>Why we ask for your name, and what we do with it:</b>
<br>
The elections form asks for your name so the Elections Chair can insure only members in good standing vote, and that they only vote once.
Only votes from people who are verified by the Elections Chair to either currently be members or will become members by the date of the Annual Meeting will be counted. 
<P>
The Elections Chair uses this information for the sole purpose of determining that the vote is valid.
The only other person with access to this information is the webmaster, who will only access it at the request of the Elections Chair in the event of problems with the online voting form.
No permanent record will be kept which associates your individual choices with your name.
If you have any questions on the elections process, please contact the Elections Chair.

<P><B>Voting on the motion:</b>
<br>You must select a choice (including not voting for it), to insure you did not forget to make a choice.

<P>This motion was brought before the membership by the Board for the purpose of aligning our legal name for tax purposes (defined by our Articles of Incorporation, filed in 1979, never revised) with our name as specified in our Bylaws (revised 1999).
<br>
The current articles can be found at <a href="http://nmnwse.org/NMNWSE_ArtInc_orig_retyped.doc">http://nmnwse.org/NMNWSE_ArtInc_orig_retyped.doc</a>. 
<br>
The current bylaws can be found at <a href="http://nmnwse.org/bl.html">http://nmnwse.org/bl.html</a>.
<br>
Our current legal name is "NETWORK FOR WOMEN IN SCIENCE AND ENGINEERING, INC."

<P><B>Voting on the Board positions:</b>
<br>For each open position (1 VP, 1 Sect, 5 At-Large), you may vote for a declared candidate listed on the ballot, or a write-in candidtate of your choice - please contact the Elections chair if you need space for more write-ins.  You may vote for a given person only once, and you must select a choice for each declared candidate (including not voting for them), to insure you did not forget to make a choice. You may vote for only 1 person for VP, only 1 person for Secretary, and up to 5 people for At-Large, where "person" is to be interpreted as declared or write-in candidate. Voting for more than allowed will invalidate your ballot.

<P><B>Required fields on the online voting form:</b>
<br>To insure you did not accidentally skip casting one of your votes, you must select an option (which includes 'no vote') for the motion and each declared candidate before the form will be accepted. If votes are missing, it will send you to an error page, which will allow you to go back and finish the form. If the form is successfully submitted, you will be sent to a 'thank you for voting' page, which will forward you to the main AM2009 page after a few seconds.

<P><B>How you know your vote was sucessfully submitted/received:</b>
<br>If the online  the form is successfully submitted, you will be sent to a 'thank you for voting' page,
which will forward you to the main AM2009 page after a few seconds. Otherwise contact the Elections Chair.

<P><B>Changing/correcting votes:</b>
<br> Contact the Elections Chair.
<P>
The Elections Chair may at her disgression attempt to resolve invalid ballots.


<P><B>Discussion of motion and/or candidates:</b>
<br>
Please feel free to email the NMNWSE listserver, NMNWSE-l@list.unm.edu, if you want to discuss the motion or the candidates. Note: all of the candidates are on that distribution, so please take that into account when framing your comments. Discussion of candidates will also take place before the voting at the Annual Meeting.


