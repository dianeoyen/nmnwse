<P>
<img src="http://nmnwse.org/Images/button.gif">&nbsp;<font size="+1"><B>Jennifer Nisengard</b></font>
<br>
Jennifer is an Environmental Project Manager for the Risk Reduction Office in the
Environmental Protection Division at Los Alamos National Laboratory (LANL).
Current and recent projects include LANL’s 2008 Site-wide Environmental
Impact Statement and Mitigation Action Plan (a National Environmental Policy
Act document), the Land Conveyance and Transfer Project, the Energy
Management Council, the Environmental Management System, and the Trails
Working Group. She is a subject matter expert for LANL’s institutional Energy
and Fuels Conservation goal and the Energy Management Council.
<br><I>Jennifer is currently an At-Large board member whose term ends this October.</I>


<P>
<img src="http://nmnwse.org/Images/button.gif">&nbsp;<font size="+1"><B>Barbara Torres</b></font>
<br>Ms. Barbara Torres is owner and CEO of Barbara Torres Consulting, a small, woman-owned business providing professional technical consulting services in the areas of business development, ethics programs, and other regulatory areas.  Ms. Torres established her company upon retirement from Northrop Grumman Corporation. Ms. Torres also has many years of active participation in such organizations as United Way, NMNWSE, and American Business Women’s Assocation, and serving as a judge in many Science and Engineering Fairs.
<br><I>Barbara was NMNWSE president in 2005.</I>


