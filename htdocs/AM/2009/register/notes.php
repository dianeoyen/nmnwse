<P><B>TECHNICAL PRESENTATION NOTES</b>
<a name="techpres">

<P>
Technical presentations on any topic in science and engineering (broadly defined to include most other fields), as well as presentations on other topics which may be of interest to women working in these fields, are welcome. Due to the limited time available for talks, poster presentations are preferred. If more talk submissions are received than time allows, the Technical Chair may relegate some talks to posters. If your employer requires you to give a talk in order to be reimbursed for attending, please specify that in the abstracts section.

<P>Post-deadline technical abstracts (ones submitted after Sep 25, 2009) may be relegated to
the poster session even if you indicate you prefer to give a talk - contact the <a href="http://nmnwse.org/AM/2009/contacts.php">Technical Chair</a> for scheduling of your
presentation.

<P>
Note students and members with exceptional circumstances are eligible to apply for a meeting scholarship (<a href="#scholarship">details below</a>), and we are offering reduced 2010 membership rates for new members attending the annual meeting.

<P><B>CONFERENCE FEES NOTES</b>

<P>
<B>Attendee registration</b> for the full meeting includes a Friday evening reception, snacks during the meeting, the wine and cheese reception during the poster session, and the conference dinner, plus covering our facilities costs. Although slightly reduced rates are offered for attendees who can only attend the technical portion, all attendees are encouraged to come for the full meeting. 

<P>Note we are also offering a reduced annual membership rates for new members attending the meeting, as indicated on the registration form.

<P>
<a name="accompany">
<B>Accompanying adult</b> (12+) registration for the full meeting includes the Friday evening reception, the wine and cheese reception during the poster session, and the conference dinner. A reduced rate is offered for those who will only join us Saturday evening. <b>Accompanying children</b> (11 and under) are also welcome to attend the Friday evening reception at no charge, but only need to be included on the registration form if they will be at the Saturday reception and dinner (even if under 4 so free, as we need to give the count to the caterer). Accompanying persons 18 and under are welcome to attend the technical sessions at no charge (if interested). Accompanying adults over 18 wishing to attend the business meeting or technical talks would need to register as attendees; however, accompanying adults registered for dinner may come to the poster session/pre-dinner reception without registering as attendees. 
If you select any "4+" persons option in the conference fees section, please use the accompanying person name field or the comment box at the end of the registration form to specify actual number(s), and multiply by the actual number(s) of people to obtain the total.

<P>
<b>Lunch and Breakfast are on your own.</b> A Continental breakfast is included with rooms at the Hacienda Vargas Bed and Breakfast (conference hotel). 
There are several places to purchase lunch in the neighborhood. 

<P><B>SCHOLARSHIP NOTES</b> 
<a name="scholarship">

<P>Students, and members with exceptional circumstances, can apply for a meeting scholarship to cover costs.
The application process is simple: check the box on the registration form indicating you want to apply
for a scholarship, and provide a brief justification in the space provided (e.g., "student presenting paper" is sufficient for student applicants) -
we will contact you if more information is needed. 

<P>Scholarships are awarded based on need and whether
presenting a paper in the technical session. The scholarship covers the conference registration, receptions, and conference dinner.
In exceptional circumstances we may be able to provide full scholarships to cover lodging, all meals, and some transportation - please indicate in the justification if you
feel your circumstances justify a full scholarship. You can also contact the 
<a href="mailto:NMNWSE_Board-l@list.unm.edu ?Subject=NMNWSE AM2009 Scholarship">NMNWSE Board</a> or the
<a href="../contacts.php">Technical Chair</a> for more information on our scholarship policy.


<P>If applying for a scholarship, after submitting the online registration form, please wait to send any payment for your conference fees until you have heard from the <a href="http://nmnwse.org/AM/2009/contacts.php">meeting registrar</a> as to your scholarship status. Only send payment after the registrar has let you know your remaining balance after subtracting the scholarship award. Please give us at least one week to review your request. 
If you do not receive an email from the registrar in that time, or if you have questions, please <a href="http://nmnwse.org/AM/2009/contacts.php">contact us</a>. 

<P>We understand that some applying for scholarships may not be able to attend the meeting without it, and if your application is not approved we will not access any penalities for cancelling your registration. We also encourage any who might fall into this category to indicate that on their application. 


<P><B>ACCOMMODATIONS NOTES</b>

<P>
<B>Accommodations are on your own.</B>
Please see the <a href="http://nmnwse.org/AM/2009/hotel.php">conference hotel page</a> for hotel contact info, and further details. The Hacienda Vargas is small, so we encourage you to reserve your accommodations early. There is no discount rate for the meeting.

