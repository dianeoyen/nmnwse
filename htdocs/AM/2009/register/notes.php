<P><B>GENERAL NOTES</b>

<P>
<B>The deadline to register online and to submit abstracts for the technical sessions is Sep 25, 2009.</B> &nbsp;Online registrations and abstracts
received by 8:00 a.m. on Sep 26, 2009 will be viewed as meeting the
deadline. 

<P><B>After Sep 25, 2009</B>, post-deadline registrations will be
accepted on a space available basis, and a late registration fee of $10.00 will be charged.
