<P>
Algodones, NM is situated on the historic Camino Real, conveniently near Albuquerque. During the lunch break on Saturday, meeting attendees will have time to tour <a href="http://www.nmmonuments.org/inst.php?inst=4">Coronado State Monument</a>, explore the galleries and shops in Algodones, or simply sit around and chat at the Inn. 

<P>
The ruins of the pueblo of Kuaua (meaning "evergreen") at <a href="http://www.nmmonuments.org/inst.php?inst=4">Coronado State Monument</a> feature arguably the finest examples of pre-Columbian mural art in the Unites States. 
Box lunches will be available if you wish to picnic at the Monument.

<P>If you are unfamiliar with the area, please visit the websites for the <a href="http://www.rrrcc.org/">Rio Rancho Regional Chamber of Commerce</a> or <a href="http://www.rioranchonm.org/">Rio Rancho Convention and Visitor's Bureau</a>.


