<P>
<img src="http://nmnwse.org/Images/button.gif">&nbsp;<font size="+1"><B>Wendee Brunish</b></font>
<br>technical staff member at Los Alamos National Laboratory since 1984, expertise is in explosion phenomenology and the containment of underground nuclear tests. A.B. in physics (Vassar College, 1975); graduate studies in astrophysics at the University of Illinois at Urbana-Champaign (M.S. 1977, Ph.D. 1981); postdoctoral research at the University of Southern California. Currently  Group Leader for the Geophysics Group in the Earth and Environmental Sciences Division.
Experience includes: member of the Laboratory Women's Committee; first chair of the Employee Advisory Committee; headed the successful effort to establish an Ombuds Office at the Lab; participated in all three of the DOE Laboratory Womens Forums; long-time member of LAWIS, participating regularly in the annual Expanding Your Horizons conference; promoted LANL involvement in MentorNet, the national e-mentoring program; currently working to bring a pilot mentoring program to EES Divsion at the Lab to add in the retention of female post-docs and early career scientists.
Wendee is strongly committed to improving the environment for women in science through educational outreach, mentoring, professional development, and policies to enhance work/life balance, and looks forward to working with NMNWSE on these issues.

                                                                                                                 

