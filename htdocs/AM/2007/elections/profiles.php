<P>
<img src="http://nmnwse.org/Images/button.gif">&nbsp;<font size="+1"><B>Diane Albert</b></font>
<br>
NMNWSE member since 1993. Regular EYH presenter and/or committee member.  Degrees in Math Education (BS, Ohio State), Metallurgical Eng. and Materials Sci. (PhD, Carnegie Mellon), and Law (UNM). Currently working as an attorney at NM's leading IP law firm in Albuquerque, Peacock Myers, PC. Holds NM secondary school teaching license in math and science and volunteers as a tutor.  Certified personal trainer and fitness specialist. Several awards including the "Governor's Award for New Mexico Outstanding Women" and "Ten Who Made a Difference in northern New Mexico".  
Committed to empowering young girls to consider choosing careers in science and technology, advocating wise water and land use, preserving, protecting, and enhancing our National Parks, preserving the history and unique culture of New Mexico, and advocating and teaching healthy lifestyles for women and children. 




