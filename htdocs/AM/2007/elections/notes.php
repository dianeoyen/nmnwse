<b>Notes:</b>


<P><b>Why we ask for your name, and what we do with it:</b>
<br>We ask for your name only so we can insure each member votes only once. This information only goes to Elections Chair, who will add your vote to the current tally and record the fact that you have voted. No permanent record will be kept which will associate your individual choices with your name, nor will that information be available to anyone but the Elections Chair.
Only votes from people who are verified by the Elections Chair to either currently be members or will become members by the date of the Annual Meeting will be counted. 
If you have any questions on the elections process, please contact the Elections Chair.

<P><B>Number of votes for each position:</b>
<br>For each open position (1 VP, 1 Sect, 5 At-Large), you may vote for a declared candidate listed on the ballot, or a write-in candidtate of your choice - please contact the Elections chair if you need space for more write-ins.  You may vote for a given person only once, and you must select a choice for each declared candidate (including not voting for them), to insure you did not forget to make a choice. You may vote for only 1 person for VP, only 1 person for Secretary, and up to 5 people for At-Large, where "person" is to be interpreted as declared or write-in candidate. Voting for more than allowed will invalidate your ballot.
The Elections Chair may at her disgression attempt to resolve invalid ballots.


<P><B>Dual listing of VP Candidates under at-large:</b>
<br>
The two candidates for the Vice-President/President-Elect position are also listed under the "At-Large" positions so that, if you wish, you can still vote to elect to the NMNWSE board the VP candidate you did not vote for as VP. 

<P><B>Why not dual listing of Sect Candidate:</b>
<br>The candidate for Secretary is already on the board as an At-Large member, and will serve out the final year of her At-Large term if not elected to the Secretary position.

<P><B>Required fields on the online voting form:</b>
<br>To insure you did not accidentally skip casting one of your votes, you must select an option (which includes 'no vote') for each declared candidate before the form will be accepted. If votes are missing, it will send you to an error page, which will allow you to go back and finish the form. If the form is successfully submitted, you will be sent to a 'thank you for voting' page, which will forward you to the main AM2007 page after a few seconds.

<P><B>How you know your vote was sucessfully submitted/received:</b>
<br>If the online  the form is successfully submitted, you will be sent to a 'thank you for voting' page,
which will forward you to the main AM2007 page after a few seconds. Otherwise contact the Elections Chair.

<P><B>Changing/correcting your vote:</b>
<br> Contact the Elections Chair.

<P><B>Discussion of candidates:</b>
<br>
Please feel free to email the NMNWSE listserver, NMNWSE-l@list.unm.edu, if you want to discuss the candidates. Note: all of the candidates are on that distribution, so requesting a side discussion might be more appropriate than an explicit airing of opinion. Discussion of candidates will also take place before the voting at the Annual Meeting.



