Clayton, New Mexico, has many scientific and historic attractions. Here are some links to help you find them:

<UL>

<LI> <a href="http://www.claytonlakestatepark.com"><B>Clayton Lake State Park</b></a> (~12 miles) and <a href="http://www.ucmp.berkeley.edu/mesozoic/cretaceous/clayton.html">Cretaceous dinosaur tracks</a> - <a href="http://www.claytonlakestatepark.com/activities.php">other activities at the park</a>
<br>&nbsp;</LI>

<LI><a href="http://www.nps.gov/cavo/"><b>Capulin Volcano National Monument</b></a> (~60 miles) - website includes <a href="http://www.nps.gov/cavo/geology.htm">geology</a>, <a href="http://www.nps.gov/cavo/naturescience/animals.htm">animals</a>, and <a href="http://www.nps.gov/cavo/naturescience/plants.htm">plants</a>, and a <a href="http://www.nps.gov/cavo/forkids/index.htm">kid's area</a>
<br>&nbsp;</LI>

<LI><a href="http://www.herzsteinmuseum.org"><B>Herzstein Memorial Museum</b></a> run by the Union County Historical Society - website includes <a href="http://www.herzsteinmuseum.org/townhistory.php">history of Clayton</a>
<br>&nbsp;</LI>

<LI><b>Clayton High School WPA Collection</b> (no web link) - open during lunch on Saturday, Oct 27, 2007, for NMNWSE Annual Meeting attendees courtesy Clayton Public Schools. <br><I>The Clayton High School campus is a living tribute to "The New Deal", particularly the Works Projects Administration (WPA). The campus was built as a WPA project in the 1930's in Pueblo Revival architectural style. A special room is dedicated to the handmade furniture, hand woven drapes, iron and copper work of many Union County residents who built the school. Thirty-six easel paintings by well-known NM artists are also on view. This collection constitutes the only WPA museum in the state.</I> 
<br>&nbsp;</LI>
                                                                                                                
<LI><a href="http://folsommuseum.netfirms.com/"><B>Folsom Museum</b></a> (~60 miles) dedicated to the history of <a href="http://folsommuseum.netfirms.com/folsomman.htm">Folsom Man</a> - while there you can also visit the <a href="http://folsommuseum.netfirms.com/folsom_falls.htm">scenic Folsom Falls</a>, about 2 miles north of Folsom on 456.
<br>&nbsp;</LI>

<LI><a href="http://nenewmexico.com/tours.php?p=2"><b>Dry Cimarron Scenic Byway driving tour</b></a>
