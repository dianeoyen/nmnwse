has been a landmark for travelers of the southwest since the 1890s. The Eklund is known for the elaborately carved bar which is still in use in the saloon.
<br>&nbsp;</LI>

<LI><a href="http://www.claytonnewmexico.net"><b>Clayton Tourism Department</b></a> - site includes a piece on <a href="http://www.claytonnewmexico.net/blackjack.html"><b>"Black Jack" Ketchum</b></a>, one of Clayton's most famous outlaws.
