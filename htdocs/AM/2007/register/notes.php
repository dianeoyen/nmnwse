<P><B>CONFERENCE FEES NOTES</b>

<P>
<B>Attendee registration</b> includes a Friday evening reception, snacks during the meeting, and covers our costs for the meeting room. 

<P>
<B>Accompanying adult</b> registration includes the Friday evening reception and a Saturday morning activity orientation. <b>Accompanying children</b> are also welcome to attend the Friday evening reception and Saturday morning activity orientation, but only need to be included on the registration form if they will be at the Saturday dinner (even if under 8 so free).

<P>
If you select any "4+" persons option in the conference fees section, please use the accompanying person name field or the comment box at the end of the registration form to specify actual number(s), and multiply by the actual number(s) of people to obtain the total.

<P>
Lunch and Breakfast are on your own, and are available at the conference hotel. 

<P><B>SCHOLARSHIP NOTES</b> 

<P>Students, and members with exceptional circumstances, can apply for a meeting scholarship to cover
costs. The application process is simple: check the box on the registration form indicting you want to apply
for a scholarship, and provide a brief justification in the space provided (e.g., "student presenting paper") -
we will contact you if more information is needed. 

<P>Scholarships are awarded based on need and whether
presenting a paper in the technical session. The standard scholarship covers only the conference fees, but
in exceptional circumstances we can provide full scholarships - please indicate in the justification if you
feel your circumstances justify a full scholarship. You can also contact the 
<a href="mailto:NMNWSE_Board-l@list.unm.edu ?Subject=NMNWSE AM2007 Scholarship">NMNWSE Board</a> or the
<a href="../contacts.php">Technical Chair</a> for more information on our scholarship policy. 

<P>If applying for a scholarship, please contact the <a href="http://nmnwse.org/AM/2007/contacts.php">meeting registrar</a> for scholarship status prior to sending a check for your conference fees - give us at least one week to review your request. 


<P><B>ACCOMMODATIONS NOTES</b>

<P>
<B>Accommodations are on your own.</B>
Please see the <a href="http://nmnwse.org/AM/2007/hotel.php">conference hotel page</a> for hotel contact info and further details.

<P>We must release any unsold rooms on C.O.B <b>1 Oct. 2007</b>. If you
do not make your hotel reservations before C.O.B. 1 Oct. 2007 and
