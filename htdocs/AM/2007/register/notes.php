<P><b>Scholarship applicants:</b> we have pre-reserved the two rooms that have two twin beds for use by scholarship applicants assuming they would want to save costs by sharing a room - contact the <a href="../contacts.php">Meeting Chair</a>, rather than the hotel, about these rooms.

