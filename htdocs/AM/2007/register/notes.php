<P><B>GENERAL NOTES</b>

<P>
<B>The deadline to register online is 1 Oct. 2007.</B> &nbsp;Online registrations and abstracts
received by 8:00 a.m. on 2 Oct. 2007 will be viewed as meeting the
deadline.

<P><B>After 1 Oct. 2007</B>, post-deadline registrations will be
accepted on a space available basis, and a late registration fee of $10.00 will be charged.
Please contact the <a href="http://nmnwse.org/AM/2007/contacts.php">registrar</a> to confirm space availability before submitting a postdeadline registration.
Post-deadline papers may be relegated to
a poster session - contact the <a href="http://nmnwse.org/AM/2007/contacts.php">Technical Chair</a> for scheduling of your
presentation.

