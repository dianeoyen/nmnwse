<P>
<img src="http://nmnwse.org/Images/button.gif">&nbsp;<font size="+1"><B>Karmen Lappo</b></font> <br>
Karmen Lappo joined NMNWSE in early 2014.  She was introduced to the network while volunteering for EYH.  She particularly appreciates the mission of the NMNWSE to provide girls with encouragement who may not have mentors as a regular presence in their lives.  Karmen has been employed at Sandia National Laboratories since 2003 as a mechanical engineer.  During her tenure, she has coordinated large scale test, performed computer simulations and served as a detailee in Washington, DC. She looks forward to an opportunity of serving as a NMNWSE board member and helping the network continue its influence on young female scientists.
<br><img src="http://nmnwse.org/Images/bl_pin.gif"><I> NMNWSE Central Chapter. Member since 2014.</I>

<P>
<img src="http://nmnwse.org/Images/button.gif">&nbsp;<font size="+1"><B>Barbara Torres</b></font> <br>
Ms. Barbara Torres received her BS and MS degrees in Physics from the University of New Mexico. She worked many years in the corporate sector and pon her retirement from Northrop Grumman, she established Barbara Torres Consulting, a small, woman-owned business providing professional technical consulting services in the areas of business development, ethics programs, and other regulatory areas..  At Northrop Grumman and its legacy companies she held leadership positions in Ethics and Business Conduct, Business Development, and project management for various scientific programs. Her primary professional activity currently is teaching as a substitute at St. Pius High School.
<P>
Ms. Torres is also active in the community.  She served for many years as a member of the United Way Community Fund Review Panel, and judged in local, regional, state, and international Science and Engineering Fairs.  She is an active member of the New Mexico Network for Women in Science and Engineering, serving on the Board of Directors for many years.  She is a 35-year member of the American Business Women's Association (ABWA) where she was recognized nationally as one of their Top Ten Business Women in 1983 and in 2006 and served as a national officer of the association for two years. 
<P>
She and her husband, Jim have lived in Tijeras, NM for for nearly 38 years.  Their two sons, James and Bill, and their families reside in Albuquerque.
<br><img src="http://nmnwse.org/Images/bl_pin.gif"><I> Current NMNWSE Treasurer. Central Chapter since 1985.</I>

<P>
<img src="http://nmnwse.org/Images/button.gif">&nbsp;<font size="+1"><B>Tinka Gammel</b></font> <br>
Tinka is theoretical Condensed Matter Physicist doing Equation of State work at Los Alamos National Lab. 
She has served as NMNWSE webmaster and listserver manager since about 2000. She moved the NMNWSE Careers book from hardcopy to the web, created the first "NMNWSE Careers CD" for the NMNWSE EYH conferences, and has overseen several revisions to the NMNWSE Careers Book and Careers CD. After 15 years as the NMNWSE webmaster - or more accurately, "web-amateur" <font size="+1">&#x263A;</font> -  she'd like to retire. Any volunteers to replace her are more than welcome, especially anyone who might actually know something about website management!
