<br>
Technical presentations on any topic in science and engineering (broadly defined to include most other fields), as well as presentations on other topics which may be of interest to women working in these fields, are welcome. 
<br>&nbsp;&nbsp;&nbsp;&nbsp;<img src="http://nmnwse.org/Images/gr_dot.gif" height=12>
Posters have no fixed size limits.
Talks are 30 minutes, including questions.
