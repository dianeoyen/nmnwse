<P> The 2016 meeting will be held at the Nativo Lodge in Albuquerque, NM.
Albuquerque is centrally located in New Mexico and the largest city in the state. Albuquerque is home many businesses, the University of New Mexico, a national lab, and several cultural attractions. Nativo Lodge is inspired by vibrant Native American culture and traditions, celebrating history through Native American influences including art, design, architecture and events. 

<P> The meeting begins with a Friday evening reception. On Saturday morning, the annual business meeting of the Network, including election of the 2016-17 Board, takes place. The Technical Sessions Saturday afternoon are an excellent opportunity for NMNWSE members to network and present their work in a supportive atmosphere (scholarships may be available to presenters - see below), with refreshments served during the poster session to further encourage networking. Saturday ends with a group dinner featuring presentation of the <B>IMPACT! Award</b> and a keynote speaker (TBA). Further details are anticipated mid-summer, 2016.
<P>If you would like to suggest a keynote speaker or provide other input for meeting planning, please use the <a href="http://nmnwse.org/AM/survey">interest survey</a>, or contact the <a href="mailto:NMNWSE_Board-L@list.unm/edu?Subject=AM 2016">NMNWSE Board</a>, or any Board member.

