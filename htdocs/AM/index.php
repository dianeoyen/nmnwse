<P> The 2016 meeting will be held at the Nativo Lodge in Albuquerque, NM.
Albuquerque is centrally located in New Mexico and the largest city in the state. Albuquerque is home to many businesses, the University of New Mexico, a national lab, and several cultural attractions. Nativo Lodge is inspired by vibrant Native American culture and traditions, celebrating history through Native American influences including art, design, architecture and events. 

<P> The meeting begins with a Friday evening reception. On Saturday morning, the annual business meeting of the Network, including election of the 2016-17 Board, takes place - all members are encouraged to attend. Lunch will include the <b>2016 NMNWSE IMPACT! Award</b> presentation, plus be a joint meal with AAUW-NM members. A Keynote speaker will follow lunch, in another joint session with AAUW-NM. The afternoon features member technical talks, followed by a poster session and reception - an excellent opportunity to network and for students to present their work in a supportive environment! Members are invited to stay though Sunday for the first meeting of the 2016-17 State Board.

