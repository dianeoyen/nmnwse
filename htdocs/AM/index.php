<P>
<img src="http://nmnwse.org/Images/bl_pin.gif"><font color="#cb6940"><b>Call for Papers:</b></font>

<br>
Technical presentations on any topic in science and engineering (broadly defined to include most other fields), as well as presentations on other topics which may be of interest to women working in these fields, are welcome. 
<br>&nbsp;&nbsp;&nbsp;&nbsp;<img src="http://nmnwse.org/Images/gr_dot.gif" height=12>
Posters have no fixed size limits.
Talks are 30 minutes, including questions.
<br>&nbsp;&nbsp;&nbsp;&nbsp;<img src="http://nmnwse.org/Images/gr_dot.gif" height=12>
Due to the limited time available for talks, <b>poster presentations are preferred</b>.
<br>&nbsp;&nbsp;&nbsp;&nbsp;<img src="http://nmnwse.org/Images/gr_dot.gif" height=12>
Talk abstracts are due Sept 12. After Sept. 12 only poster presentations are accepted
<br>&nbsp;&nbsp;&nbsp;&nbsp;<img src="http://nmnwse.org/Images/gr_dot.gif" height=12>
Poster abstracts due Oct 3 (post-deadline posters accepted on a space-available basis - contact the Technical Chair).
<br>&nbsp;&nbsp;&nbsp;&nbsp;<img src="http://nmnwse.org/Images/gr_dot.gif" height=12>
To submit a presentation, once registration opens, simply include the draft title and abstract with your meeting registration. 
<br>&nbsp;&nbsp;&nbsp;&nbsp;<img src="http://nmnwse.org/Images/gr_dot.gif" height=12>
You will be contacted for final title and abstract around Oct 7.
<br>&nbsp;&nbsp;&nbsp;&nbsp;<img src="http://nmnwse.org/Images/gr_dot.gif" height=12>
Note students, teachers, and non-employed members giving posters are eligible to apply for a meeting scholarship to cover registration. For those with exceptional circumstances, full scholarships to cover all costs can be requested.  

