<P>
The Santa Fe-fall planning committee is actively seeking presenters and other volunteers to help with the SF-fall EYH on Saturday, September 19, 2015, at Santa Fe Community College (SFCC). We are planning for 150 to 170 girls (grades 5-8) to come explore careers in Science, Technology, Engineering, and Math (STEM). The girls will attend two hands-on workshops, hear a dynamic keynote speaker, and participate a STEM fair. Adults (teachers and parents) can also register to attend the conference that includes a two-hour workshop led by Phyllis Baca. 
</P>

