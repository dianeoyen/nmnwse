<img src="http://nmnwse.org/Images/r_hand.gif">&nbsp;<b><font color="#cb6940" size="+1">Detailed Conference Information:</font></b>
<P>
 In 2010, which will be the 18<sup>th</sup> annual Silver City EYH conference, we will be targeting grades 5-12 and registration will limited to about 350 students. 
We will continue with three workshops, a pizza lunch, a closing activity and free t-shirts for the students attending the conference.
We are working on improving the experience for attendees in high school, and tentatively will offer some focused presentations geared towards students who are getting ready for college.

<P>
No flyers or brochures are available yet - anticipated Jan 2010.
<P>
Contact <a href="mailto:ewarner@dishmail.net">Emily Warner</a>, or any of the above contacts, if you'd like to get involved with Silver City EYH conference planning or have any questions. See also the <a href="http://nmnwse.org/eyh">NMNWSE EYH</a> and <a href="http://www.expandingyourhorizons.org">national EYH</a> webpages for <a href="http://nmnwse.org/eyh/eyh_confs.php">other EYH conferences in New Mexico</a> and background on these conferences.


