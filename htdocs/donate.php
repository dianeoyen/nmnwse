NMNWSE provides a professional development, networking, and mentoring resource
for women and girls in New Mexico who are working or interested in STEM
(Science, Technology, Engineering, Mathematics) and allied professions
 including the biological, behavioral, medical, and social sciences.
Our major membership activity is an
<a href="http://nmnwse.org/AM">annual meeting and technical symposium</a>
held each fall where members and students present papers and network.
In cooperation with the New Mexico Governor's Commission on the Status of Women,
we give out an annual
<a href="http://nmnwse.org/ImpactAward">IMPACT! Award</a>
recognizing outstanding New Mexico women.
We also host local chapter activities such as talks and meetings.
Our major outreach activity is holding annual
<a href="http://nmnwse.org/eyh">Expanding Your Horizons (EYH)</a>
technical career workshops for young women,
licensed from the National EYH Network.
<! 20,000 in 2008, roughly 1000/yr additional >
Since 1980 over 25,000 students and their teachers have attended the free or low-cost NMNWSE EYH conferences.
We publish and provide to all EYH attendees a copy of
"Careers: Exploring the Possibilities",
which is also available for free online at <a href="http://nmnwse.org/careers">http://nmnwse.org/careers/</a>.
We also sponsor student awards at science fairs and similar technical competitions.

