<a name="PRO1"></a>
<H3>
97-PRO1. FINANCES
</H3>

All income received by the Network is received by the Treasurer and deposited in the checking account within one week. All bills for payment or reimbursement are to be provided to the Treasurer, who is to make payment from the Network checking account in a timely manner.

<P>
The Treasurer maintains records of income and expenditure according to the budget categories that are approved by the Network membership at the annual meeting. A written report is presented at each Board meeting identifying 1) income and expenditures for the current reporting period and for the year-to-date according to the approved budget categories and 2) money on hand in the checking account and savings account.

<P>
Books close September 30. All bills for the fiscal year must be submitted prior to that date for reimbursement.

<P>
At the end of each fiscal year the Treasurer submits the financial records of the Network for audit according to Article VII, Section 7.5 of the Network Bylaws. The financial status of the Network and the report of the Auditing Committee are presented at the annual meeting.

<a name="PRO2"></a>
<H3>
08-PRO2. ELECTIONS
</H3>

The Chair of the Election Committee supervises the establishment of a slate of candidates. In accomplishing this, she must become familiar with Article IV, Sections 4.1 through 4.3 and Article V of the Network Bylaws and have available a list of the terms of the current members of the Board.

<P>
The nominating process begins in the spring. The slate of candidates is to be completed and the ballot posted on the Network's website at least one month prior to the Annual Meeting, with voting to close prior to the Annual Meeting. The Chair of the Election Committee appoints others to count or counts with others the ballots received. A list of electronic voters is provided to the Secretary prior to the Annual Meeting. Additional votes will be accepted at the Annual Meeting and added to the electronic tally. The election results are announced at the annual meeting.

<a name="PRO3"></a>
<H3>
08-PRO3. NETWORK NEWS
</H3>

Network News is posted to the Network Website by the Communications Committee as it is received  The Communications Committee seeks news items and solicits contributions of articles  and web sites of interest to the membership. 

<P>
An annual newsletter is published two (2) months prior to the Annual Meeting.  In order to assure that this newsletter is distributed in a timely fashion, labels are requested from the Membership Chair two weeks prior to mailing the newsletter. The Communications Committee mails the newsletter, material from the Annual Meeting Chair, and membership forms provided by the Membership Chair.

<a name="PRO4"></a>
<H3>
08-PRO4. ANNUAL MEETING
</H3>

The annual meeting of the Network is held in the fall during October. The Annual Meeting Committee is responsible for  the following:

<OL><LI>
   Developing and presenting a budget, date, and location for the meeting at the June meeting of the Board.
</LI><LI>
   Opening a bank account, if needed.
</LI><LI>
   Providing information on the Annual Meeting and Technical Symposium to the Communications Chair, who posts it on the web site.
</LI><LI>
   Preparing and distributing a request for papers in June.
</LI><LI>
   Preparing a technical program.
</LI><LI>
   Making arrangements for any hotel, meals, meeting rooms and presentation needs.
</LI><LI>
   Publishing and distributing promotional material for the annual meeting through the Network website, email, and U.S, mail.
</LI><LI>
   Accounting for registration of attendees from on-line registration.
</LI><LI>
  Inviting speakers and taking care of their needs.
</LI><LI>
  Submitting a complete, written financial statement of income and expenses for the meeting to the Board.
</LI></OL>

<P>
The Network will provide annual meeting scholarships to unemployed or student members as approved in the budget. The Annual Meeting Committee will administer the scholarships.

