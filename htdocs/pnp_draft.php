The Network web site is maintained on an "as-needed" basis, with pertinent news, announcements, resource links, and any other information as desired by members or suggested by the Communications chair.

<P>
Content may be placed on the site at the discretion of the Communications Committee. The Board will decide questions of appropriate content.

<P>
The domain name, NMNWSE.ORG, was registered originally in November 1998 to "Network Solutions, Inc".

<a name="PRO10"></a>
<H3>
08-PRO10.  ONLINE VOTING
</H3>

As stated in <a href="#POL8">08-POL8</a>, ONLINE VOTING, any member of the Network Board may bring a motion to the Executive Committee on-line using the list-server (<a href="mailto:NMNWSE_BOARD-L@list.unm.edu">NMNWSE_BOARD-L@list.unm.edu</a>).  After a motion is posted and a second is received, or if the motion comes from a committee, discussion may continue via the list-server.   Board members will have a number of days determined by the President to comment on and discuss the issue.  After the end of that time, the president will call for a vote.  Board members will then have a number of days set by the President to submit their votes.


