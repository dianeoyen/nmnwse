<a name="PRO7"></a>
<H3>
97-PRO7. FUND RAISING
</H3>

A letter is mailed each year to New Mexico businesses at the discretion of the Fund Raising Chair. A standard form serves as a record keeper for each company. This form contains information such as: company name, address, contact person, the date the letter was sent, replies, comments and date of thank-you letter.

<P>
All fund raising efforts, including but not limited to chapter and EYH activities, shall be coordinated with the State Board Fund Raising Chair to avoid duplication of effort and to maintain an accurate record of donations.

<a name="PRO8"></a>
<H3>
08-PRO8. STUDENT AWARDS
</H3>

The Network shall provide awards at the State Science and Engineering Fair and other student competitions as approved in the budget.  Prior to the Annual Meeting, the Chair of the Student Awards Committee shall present recommendations on awards to be made by the network to the Treasurer for inclusion in the proposal budget.  These recommendations shall include: the competitions at which awards will be given, how many awards for each competition, and the content of the awards (dollar values and other items).

<P>
Awards at the New Mexico State Science and Engineering Fair are typically included in the Network awards.  The Chair of the Student Awards Committee identifies the date and exhibit categories for the Science and Engineering Fair and obtains the necessary award forms, awards and judges' names prior to the deadline (mid-February). Information on the Science Fair is obtained from
<blockquote>
    New Mexico Science and Engineering Fair <br>
    New Mexico Institute of Mining and Technology <br>
    Campus Station, P. 0. Box 3732 <br>
    Socorro, NM 87801
</blockquote>

<P>
The Student Awards Committee chair selects a panel of judges and supervises the selection of the award winners. It is strongly suggested that the names of entrants in the selected categories be obtained prior to the Science and Engineering Fair so that judging assignments can be made. Names, addresses and social security numbers for the winners are obtained and furnished to the Treasurer. The chair then coordinates the presentation of the awards to the winners.

