<H2 ALIGN="CENTER">
EXECUTIVE COMMITTEE
<br>
DUTIES AND RESPONSIBILITIES
</H2>


The Executive Committee of the Network for Women in Science and Engineering (hereafter referred to as the Network) consists of the officers of the Board of Directors (hereafter referred to as the Board). These officers are the President, the immediate Past President, the Vice President/President-Elect, the Secretary, and the Treasurer.

<H3>PRESIDENT
</H3>
<OL><LI>
   Coordinates and chairs the meetings of the Board.
</LI>
<OL TYPE="a">
<LI>
Arranges for meeting location.
</LI><LI>
Establishes the agenda.
</LI><LI>
Sends out agenda and minutes of the previous meeting prior to the meeting.
</LI>
</OL>
<LI>
   Chairs the Executive Committee of the Board.
</LI><LI>
   Represents the Network or designates an alternate to represent the Network.
</LI><LI>
   Represents the Board at Expanding Your Horizons (EYH) Conferences.
</LI><LI>
   Disburses Network funds in the absence of the Treasurer.
</LI><LI>
   Chairs the annual meeting of the Network.
</LI><LI>
   Contributes to furthering the work of the Board and the Network. 
</LI></OL>

