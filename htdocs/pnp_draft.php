<a name="POL4"></a>
<H3>
08-POL4. MEMBERSHIP DATABASE
</H3>

The membership database is in an electronic database. Information on a new member is added within three days of receiving the completed membership form. Renewals in the database show the new expiration date and any new information that was shown on the renewal form.

<P>
Names of non-members may be added periodically to allow them to receive information about the membership. These names will be maintained in the database for six months unless the individual joins the Network. If the person does not join after six months the name may be deleted from the database.

<P>
The membership information is kept confidential. The mailing list is not supplied outside of the Network.


<a name="POL5"></a>
<H3>
08-POL5. REIMBURSEMENT FOR EXPENSES
</H3>

<P>
Board members are encouraged to car pool for Board meetings outside their home city.

<P>
However all Board members or delegates will be reimbursed for travel whether or not they are able to car pool. Transportation will be reimbursed at cost or IRS non-profit mileage rate. Hotel accommodations will also be reimbursed. Reimbursement will be made upon submittal of an expense statement to the treasurer. If the travel budget has been exceeded, the reimbursement may require Board approval.

<P>
Board members will be reimbursed for long distance calls and other expenses such as postage as approved by the Board upon submittal of an expense statement to the treasurer.

<P>
Board members will be reimbursed for other expenses approved by the Board. After approval, reimbursement will be made upon submittal of an expense statement to the treasurer.

<a name="POL6"></a>
<H3>
08-POL6.  LOCAL CHAPTERS
</H3>

Local Chapters can be formed in locations where members of the New Mexico Network for Women in Science and Engineering reside.  Members of each local chapter shall be members in good standing of the state Network.

<P>
Each active chapter will have a voting member on the Network Board who will attend Board meetings and provide a written report prior to or at the Board meeting.

<P>
Annually, the Treasurer shall issue to each active chapter funding in an amount determined during the development of the annual budget <a href="#POL2">(08-POL2. CHAPTER FUNDING)</a>. 

<P>
Chapters shall hold chapter board elections at least once every two years.  

<P>
Chapters shall hold at least one chapter board meeting and one meeting of the entire chapter membership during each calendar year.  These meetings shall be "in-person," but will not exclude virtual attendance by remote connection (e.g. teleconferencing).  Additional meetings may be virtual or "in person."

<P>
Chapters shall use the Network logo on all websites and promotional material.  This does not exclude a chapter and/or event logo in addition to the Network logo.

<a name="POL7"></a>
<H3>
08-POL7. EXPANDING YOUR HORIZONS (EYH) CONFERENCES
</H3>

<P>
EYH Conference chairs shall be Network members in good standing.  Organizing committee members and EYH volunteers shall be given Network membership information, which shall be provided by the Network at no cost to the local EYH committee.

<P>
New Mexico EYH conferences may be co-hosted and/or co-sponsored by other organizations.  

<P>
Annually, the Treasurer shall issue to each EYH committee funding in an amount determined during the development of the annual budget. 

<P>
In addition to any other logos, local EYH conference websites and advertising flyers shall use the Network logo and shall state that the conferences is "licensed by the New Mexico Network for Women in Science and Engineering from the EYH Network."

<P>
Statewide sponsors of Network EYH conferences shall be acknowledged whenever and wherever local sponsors are acknowledged.  The list of these sponsors shall be available on the Network website and from the statewide EYH Coordinator.

<a name="POL8"></a>
<H3>
08-POL8. VIRTUAL, ON-LINE MEETINGS AND VOTING
</H3>

In addition to the scheduled "in-person" meetings, the Network Board may hold virtual, on-line meetings to address time-sensitive topics.

<P>
Any member of the Network Board may bring a motion to the Executive Board on-line using the list-server (<a href="mailto:NMNWSE_BOARD-L@list.unm.edu">NMNWSE_BOARD-L@list.unm.edu</a>).  Action on the motion will proceed as described in <a href="#PRO10">08-PRO10</a>, ONLINE VOTING.


