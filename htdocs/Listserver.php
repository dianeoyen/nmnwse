<p>Welcome to the New Mexico Network for Women in Science and Engineering
discussion list.  The purpose of this list is to facilitate communication with and
among members of the Network.  Appropriate postings to the list include
conference and meeting announcements, job postings, and other
Network-related business.  You do not need to be an NMNWSE member to subscribe. List subscribers who post inappropriate
material will be unsubscribed by the list manager.  </p>

<p>For those of you who have not been members of listserv lists at UNM
before, what follows are instructions on how to post, unsubscribe, and get
information about the list. &nbsp;We hope this list will be a useful tool to members of the Network. </p>

<P> <img src="http://nmnwse.org/bl_pin.gif">
<font color="#4b9fc3"><B><U>TO SEND A MESSAGE TO THE LIST</U></B></font>

<P>
Once members are subscribed, to communicate with one another simply
address messages to<br>
&nbsp; &nbsp; &nbsp; &nbsp; <a href="mailto:NMNWSE-L@list.unm.edu"><B>NMNWSE-L@list.unm.edu</B></a>
<br> Emails are usually auto-forwarded to the list within mintues, but occasionally can take up to a few hours if traffic is heavy or there are server problems.
<p> You can also send messages to the list if you are not subscribed, but the message will have to be approved by the list manager. Since this person may be busy or on vacation, if urgent we suggest you subscribe, post the email, confirm you get a copy, then unsubscribe (if you do not wish to remain on the list).

<P> <img src="http://nmnwse.org/bl_pin.gif">
<font color="#4b9fc3"><B><U>TO SUBSCRIBE</U></B></font>

<P>
From the email account you wish to subscribe, send a message to:<br>
&nbsp; &nbsp; &nbsp; &nbsp; <B>listserv@list.unm.edu</B><br>
Leave the Subject field blank. &nbsp;
In the body of message type (with no other text):<br>
&nbsp; &nbsp; &nbsp; &nbsp; <B>subscribe NMNWSE-L Firstname Lastname</B>
<br>&nbsp; &nbsp; &nbsp; &nbsp; <B>end</B>
<br>You will receive an automated message to the email you subscribed confirming you were added. If you do not receive this email (usually within minutes, but can take up to a few hours), please contact the <a href="mailto:webmaster@nmnwse.org ?Subject=NMNWSE-L subscribe attempt failed">NMNWSE webmaster</a>.

<P> <img src="http://nmnwse.org/bl_pin.gif">
<font color="#4b9fc3"><B><U>TO UNSUBSCRIBE</U></B></font>

<P>
From the email account you wish to unsubscribe, send a message to:<br>
&nbsp; &nbsp; &nbsp; &nbsp; <B>listserv@list.unm.edu</B><br>
Leave the Subject field blank. &nbsp;
In the body of message type (with no other text):<br>
&nbsp; &nbsp; &nbsp; &nbsp; <B>signoff NMNWSE-L</B>
<br>&nbsp; &nbsp; &nbsp; &nbsp; <B>end</B>
<br>You will receive an automated message to the email you unsubscribed confirming you were removed. If you do not receive this email (usually within minutes, but can take up to a few hours), or you no longer have access to the account you wish to unsubscribe, please contact the <a href="mailto:webmaster@nmnwse.org ?Subject=NMNWSE-L unsubscribe attempt failed">NMNWSE webmaster</a>.

